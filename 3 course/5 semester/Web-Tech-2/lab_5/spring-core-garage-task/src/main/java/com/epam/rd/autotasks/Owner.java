package com.epam.rd.autotasks;

public class Owner {
    private String name;
    private String taxNumber;

    public Owner() { }

    public String getName() {
        return name;
    }

    public String getTaxNumber() {
        return taxNumber;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setTaxNumber(String taxNumber) {
        this.taxNumber = taxNumber;
    }
}
